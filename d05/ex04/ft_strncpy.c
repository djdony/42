char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*tmp;
	int		i;

	i = 0;
	tmp = dest;
	while (i <= n && src[i] != '\n')
	{
		tmp[i] = src[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
